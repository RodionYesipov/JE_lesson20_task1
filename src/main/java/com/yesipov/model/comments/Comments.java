package com.yesipov.model.comments;

public class Comments {

    public Integer postId;
    public Integer id;
    public String name;
    public String email;
    public String body;

    public Comments() {
    }

    public Comments(Integer postId, Integer id, String name, String email, String body) {
        this.postId = postId;
        this.id = id;
        this.name = name;
        this.email = email;
        this.body = body;
    }

    @Override
    public String toString() {
        return "Comments{" +
                "postId=" + postId +
                ", id=" + id +
                ", name='" + name + '\'' +
                ", email='" + email + '\'' +
                ", body='" + body + '\'' +
                '}';
    }
}